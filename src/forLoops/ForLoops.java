package forLoops;

public class ForLoops {

	public static void main(String[] args) {
		for (int i = 0; i < 5; i++) 
			System.out.println("Hello World " + i);
		
		for (int j = 1; j <= 5; j++) 
			System.out.println("Hello World 2 " + j);
		
		for (int w = 5; w > 0; w--) 
			System.out.println("Hello World 3 " + w);
	}

}
